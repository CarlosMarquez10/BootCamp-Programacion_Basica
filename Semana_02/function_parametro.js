// las funciones de con parametros es una funcion que recibe valores llamado argumentos cuando se llama, y los usa dentro de su cuerpo para realizar alguna operacion.


// explicacion del la funcion

function saludar(nombre){ // la variable nombre aqui en esta posicion seria el parametro
    console.log("Hola, "+ nombre + "...!")
}

// cuando llamamos la funcion de la siguiente manera

saludar("carlos"); // el valor que le estamos pasando se ve como argumento y no como parametro. 



